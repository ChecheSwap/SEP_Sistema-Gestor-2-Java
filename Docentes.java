public class Docentes {
    String curp;
    String estatus;
    String apellidoP;
    String apellidoM;
    String nombre1;
    String nombre2;
    String sexo;
    String telefono;
    String municipio;
    String estado;

    public Docentes(String curp, String estatus, String apellidoP, String apellidoM, String nombre1, String nombre2, String sexo, String telefono, String municipio, String estado) {
        this.curp = curp;
        this.estatus = estatus;
        this.apellidoP = apellidoP;
        this.apellidoM = apellidoM;
        this.nombre1 = nombre1;
        this.nombre2 = nombre2;
        this.sexo = sexo;
        this.telefono = telefono;
        this.municipio = municipio;
        this.estado = estado;
    }

    
    
    
}
